#include "computor.h"

int	ft_ajust(int rank, double *x)
{
	int	i;
	int	j;

	i = 0;
	j = -1;
	while (i <= rank)
	{
		if (x[i] != 0)
			j = i;
		i += 1;
	}
	if (j >= 0)
		rank = j;
	else
		rank = 0;
	return (rank);
}
